package io.damelyngdoh.java.trie;

/**
 * <p>
 * Traversal method for the depth-first search. it refers to whether a method 
 * uses a loop structure or calls itself recursively to traverse the tree. 
 * <tt> Incremental</tt> method will use a loop structure while <tt>Recursive</tt> 
 * will be a recursive traversal.
 * 
 * @author Dame Lyngdoh
 * @since 1.0.0
 */
public enum TrieTraversal {
    Incremental, Recursive
}
